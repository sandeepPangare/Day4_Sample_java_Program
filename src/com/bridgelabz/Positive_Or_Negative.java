package com.bridgelabz;
public class Positive_Or_Negative {
    public static void main(String[] args) {

        int n=11;

        if(n > 0)
        {
            System.out.println(n + "Positive Number");
        }
        else if(n < 0)
        {
            System.out.println(n + "Negative Number");
        }
        else
        {
            System.out.println("zero");
        }

    }
}
